

//  Assignment Operators :- 

// =
x = 10;
console.log(x)
y = x
console.log(y)

// +=
z = 20
console.log(z)
z += 20
console.log(z)

// -=
n = 20
console.log(n)
n -= 5
console.log(n)

// /=
s = 20
console.log(s)
s /= 5
console.log(s)

// %=
p = 27
console.log(p)
p %= 5
console.log(p)

// **=
l = 2
console.log(l)
l **= 3
console.log(l)


// Comparison Operators:- 

// ==
let comp1 = 6;
let comp2 = 7;
console.log("comp1 == comp2",comp1 == comp2)

// !=
console.log("comp1 != comp2",comp1 != comp2)

// ===
console.log("comp1 === comp2",comp1 === comp2)
const q = 10;
const t = 10;
console.log("Type of q = ",typeof q)
console.log("Type of t = ",typeof t)
console.log("q === t",q === t)
// this operator Compare type and value

// !==
console.log("comp1 != comp2",comp1 !== comp2)
const c = 25;
let d = 25;
console.log("Type of c = ",typeof c)
console.log("Type of d = ",typeof d)
console.log("c !== d",c !== d)

// <= & >=
console.log("10 > 20",10>20)
console.log("10 < 20",10<20)
console.log("10 >= 20", 10>=20)
console.log("10 <= 20", 10<=20)


